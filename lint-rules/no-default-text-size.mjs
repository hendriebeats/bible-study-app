/**
 * `local/no-default-text-size`
 *
 * Ban the Tailwind default font-size utilities (`text-xs`, `text-sm`,
 * `text-base`, ..., `text-9xl`) in favor of the semantic type scale defined in
 * `src/app/globals.css` (`text-caption`, `text-ui`, `text-body`, ...). Pairs
 * with `local/no-inline-font-size` and `scripts/check-css-font-size.mjs` to
 * lock down every surface where a font-size can hide.
 *
 * Detection: walks JSX `className` attribute values — plain string literals,
 * template literals (the static quasi parts), and string arguments to
 * `cn(...)` / `clsx(...)` / `cva(...)` (and their first-arg variants config
 * object for cva, where the base classes live).
 *
 * Replacement map (matches scripts/migrate-text-tokens.mjs):
 *   text-xs   → text-caption     text-2xl  → text-title
 *   text-sm   → text-ui          text-3xl  → text-page-title
 *   text-base → text-body        text-4xl  → text-display
 *   text-lg   → text-subheading  text-6xl  → text-display-xl
 *   text-xl   → text-heading
 *
 * `text-5xl`, `text-7xl`, `text-8xl`, `text-9xl` have NO suggested
 * replacement — they aren't in the semantic scale and the error tells you to
 * add a `--text-*` token if you actually need a new size.
 *
 * shadcn/ui primitives under `src/components/ui/**` are exempted in
 * eslint.config.mjs (they get regenerated by the shadcn CLI).
 */

const REPLACEMENTS = {
  "text-xs": "text-caption",
  "text-sm": "text-ui",
  "text-base": "text-body",
  "text-lg": "text-subheading",
  "text-xl": "text-heading",
  "text-2xl": "text-title",
  "text-3xl": "text-page-title",
  "text-4xl": "text-display",
  "text-6xl": "text-display-xl",
};

// No semantic equivalent — error without a suggestion so the dev makes a
// deliberate choice (add a new --text-* token, or scale back).
const BANNED_OVERSIZED = new Set([
  "text-5xl",
  "text-7xl",
  "text-8xl",
  "text-9xl",
]);

const ALL_BANNED = new RegExp(
  `\\b(${[...Object.keys(REPLACEMENTS), ...BANNED_OVERSIZED].join("|")})(?![-\\w])`,
  "g",
);

const CLASSNAME_HELPER_NAMES = new Set([
  "cn",
  "clsx",
  "classnames",
  "cva",
  "tw",
]);

/** @type {import('eslint').Rule.RuleModule} */
const rule = {
  meta: {
    type: "problem",
    docs: {
      description:
        "Ban Tailwind default font-size utilities; use the semantic scale (--text-*) in globals.css instead.",
    },
    schema: [],
    messages: {
      hasReplacement:
        "`{{found}}` is a Tailwind default — use the semantic `{{replacement}}` (backed by `--{{replacement}}` in src/app/globals.css).",
      noReplacement:
        "`{{found}}` is a Tailwind default with no semantic equivalent in the type scale. If a size like this is genuinely needed, add a new `--text-*` token to the `@theme inline` block in src/app/globals.css and use that.",
    },
  },
  create(context) {
    function reportString(node, text) {
      const matches = text.matchAll(ALL_BANNED);
      for (const m of matches) {
        const found = m[1];
        if (BANNED_OVERSIZED.has(found)) {
          context.report({
            node,
            messageId: "noReplacement",
            data: { found },
          });
        } else {
          context.report({
            node,
            messageId: "hasReplacement",
            data: { found, replacement: REPLACEMENTS[found] },
          });
        }
      }
    }

    // Visits an expression that's expected to evaluate to a className string.
    // Only descends through "purely structural" wrappers (templates, ternaries,
    // logical-ANDs, arrays) — NOT into call expressions, because the
    // `CallExpression` visitor below catches `cn(...)` / `clsx(...)` / `cva(...)`
    // globally regardless of where they appear. Splitting the work this way
    // avoids double-reporting when `<div className={cn("text-sm")}>` would
    // otherwise be visited by both paths.
    function visitClassNameValue(valueNode) {
      if (!valueNode) return;
      if (valueNode.type === "Literal" && typeof valueNode.value === "string") {
        reportString(valueNode, valueNode.value);
        return;
      }
      if (valueNode.type === "TemplateLiteral") {
        for (const q of valueNode.quasis) {
          reportString(valueNode, q.value.raw);
        }
        for (const e of valueNode.expressions) visitClassNameValue(e);
        return;
      }
      if (valueNode.type === "ConditionalExpression") {
        visitClassNameValue(valueNode.consequent);
        visitClassNameValue(valueNode.alternate);
        return;
      }
      if (valueNode.type === "LogicalExpression") {
        visitClassNameValue(valueNode.right);
        return;
      }
      if (valueNode.type === "ArrayExpression") {
        for (const el of valueNode.elements) visitClassNameValue(el);
      }
    }

    return {
      JSXAttribute(node) {
        if (node.name.type !== "JSXIdentifier") return;
        if (node.name.name !== "className" && node.name.name !== "class")
          return;
        const v = node.value;
        if (!v) return;
        if (v.type === "Literal") {
          reportString(node, typeof v.value === "string" ? v.value : "");
        } else if (v.type === "JSXExpressionContainer") {
          visitClassNameValue(v.expression);
        }
      },
      // Catches `cn(...)` / `clsx(...)` / `cva(...)` calls everywhere — both
      // inline in a className and assigned to a const for later reuse. Walking
      // arg objects (cva variants) and arrays handles all the common shapes.
      CallExpression(node) {
        const callee = node.callee;
        const name =
          callee.type === "Identifier"
            ? callee.name
            : callee.type === "MemberExpression" &&
                callee.property.type === "Identifier"
              ? callee.property.name
              : null;
        if (!name || !CLASSNAME_HELPER_NAMES.has(name)) return;
        for (const arg of node.arguments) {
          if (arg.type === "ObjectExpression") {
            for (const prop of arg.properties) {
              if (prop.type !== "Property") continue;
              visitClassNameValue(prop.value);
            }
          } else if (arg.type === "ArrayExpression") {
            for (const el of arg.elements) visitClassNameValue(el);
          } else {
            visitClassNameValue(arg);
          }
        }
      },
    };
  },
};

export default rule;
